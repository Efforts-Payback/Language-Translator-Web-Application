  // Populate dropdowns
  window.onload = () => {
    const fromSelect = document.getElementById("from-language");
    const toSelect = document.getElementById("to-language");
  
    for (let lang in languages) {
      let option1 = document.createElement("option");
      option1.text = lang;
      option1.value = languages[lang];
      fromSelect.add(option1);
  
      let option2 = document.createElement("option");
      option2.text = lang;
      option2.value = languages[lang];
      toSelect.add(option2);
    }
  };
  
  function translateText() {
    const fromLang = document.getElementById("from-language").value;
    const toLang = document.getElementById("to-language").value;
    const inputText = document.getElementById("input-text").value;
  
    if (!fromLang || !toLang || !inputText) {
      alert("Please select languages and enter text.");
      return;
    }
  
    fetch("http://127.0.0.1:5000/translate", {
      method: "POST",
      headers: {
        "Content-Type": "application/json"
      },
      body: JSON.stringify({ text: inputText, src: fromLang, dest: toLang })
    })
    .then(response => response.json())
    .then(data => {
      document.getElementById("output-text").value = data.translated_text;
    })
    .catch(error => {
      console.error("Translation error:", error);
      document.getElementById("output-text").value = "Translation failed.";
    });
  }
  